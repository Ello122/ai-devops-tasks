const ipv4Regex = /^((25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)\.){3}(25[0-5]|2[0-4]\d|1\d\d|[1-9]?\d)$/;

const tests = [
  "192.168.1.1",
  "10.0.0.1",
  "255.255.255.255",
  "256.1.1.1",
  "192.168.1",
  "abc.def.1.1",
  "192.168.1.999"
];

tests.forEach(ip => {
  console.log(ip, "=>", ipv4Regex.test(ip));
});